<h1>Digite um número de 0 a 7:</h1>
let numero = 3

switch (numero) {
    case 0:
        console.log('A')
        break;

    case 1:
        console.log('B')
        break;

    case 2:
        console.log('C')
        break;

    case 3:
        console.log('D')
        break;

    case 4:
            console.log('A')
            break;
    
    case 5:
            console.log('B')
            break;
    
    case 6:
            console.log('C')
            break;
    
    case 7:
            console.log('D')
            break;

    default:
        console.log('Número fora do intervalo')
        break;
}